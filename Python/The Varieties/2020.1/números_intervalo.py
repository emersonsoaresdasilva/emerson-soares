#Primeira Forma (Preferencial)
Numero = float(input("Digite o número: "))
if(1<=Numero<=100):
    print("Está dentro do intervalo")

#Segunda Forma
Numero = float(input("Digite o número: "))
if(1<=Numero and Numero<=100):
    print("Está dentro do intervalo")

#Terceira Forma
Numero = float(input("Digite o número: "))
if(Numero >=1 and Numero<=100):
    print("Está dentro do intervalo")

